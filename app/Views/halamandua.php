 <br>
To upload a manuscript to this journal, complete the following steps. <br>
1. On this page, click Browse (or Choose File) which opens a Choose File window for locating the file on the hard drive of your computer. <br>
2. Locate the file you wish to submit and highlight it. <br>
3. Click Open on the Choose File window, which places the name of the file on this page. <br>
4. Click Upload on this page, which uploads the file from the computer to the journal's web site and renames it following the journal's conventions. <br>
5. Once the submission is uploaded, click Save and Continue at the bottom of this page. <br>
<br>
Encountering difficulties? Contact for assistance.
